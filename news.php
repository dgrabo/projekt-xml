    	            <h5>Microsoft: 34% of the BMG Foundation Trust</h5>
                    <p class="text-start">Microsoft reported impressive financial results in the third quarter of fiscal 2024 (ended March 31). Revenue rose 17% to $61.9 billion and GAAP net income jumped 20% to $2.94 per diluted share. Momentum in enterprise software and cloud computing, fueled by demand for artificial intelligence services, were the driving forces behind that strong performance.</p>

                    <p class="text-start">The bull case for Microsoft is straightforward. It is the largest enterprise software company and the second largest cloud services provider in the world, and those market are projected to grow at annual rates of 14% and 21%, respectively, through 2030. Additionally, Microsoft is creating new revenue streams in both segments with artificial intelligence products like Microsoft 365 Copilot and Azure OpenAI Service.</p>

                    <p class="text-start">Going forward, Wall Street expects Microsoft to grow earnings per share at 13.7% annually over the next three to five years. That estimate leaves room for upside given that Microsoft has yet to monetize AI software and services in earnest. However, if we assume Wall Street is correct, the current valuation of 36 times earnings is somewhere between reasonable and expensive.</p>

                    <p class="text-start">I say that because the PEG ratio -- the price-to-earnings multiple divided by forecasted earnings growth -- is currently 2.6, a premium to the three-year average of 2.4. Personally, I would wait for a slightly cheaper price before buying this stock, but I think patient investors can purchase a small position today.</p>
                    
                    <h5>Berkshire Hathaway: 16% of the BMG Foundation Trust</h5>
                    <p class="text-start">Berkshire Hathaway reported solid first-quarter financial results. Revenue rose 5.2% to $90 billion on strong momentum across its insurance subsidiaries. Meanwhile, operating earnings (which eliminates the impact of investment gains and losses) increased 39% to $11.2 billion, beating even the highest estimate from Wall Street.</p>
                    <p class="text-start">The bull case for Berkshire centers on is its strong presence in property and casualty insurance, which provides CEO Warren Buffett with a consistent stream of cash in the form of premiums. Buffett has invested that capital to great effect over the years. As proof, Berkshire's book value per share increased at 11% annually over the past decade.</p>
                    <p class="text-start">Berkshire also owns several dozen subsidiaries that operate across a diverse range of industries, including freight rail transportation, utilities, energy, manufacturing, and retail. Many of those subsidiaries provide essential goods and services, which makes Berkshire a resilient business. For that reason, the stock has consistently outperformed the S&P 500 during bear markets, as shown in the chart below.</p>

                    <h5>Waste Management: 16% of the BMG Foundation Trust</h5>
                    <p class="text-start">Waste Management reported mixed results in the first quarter. Revenue increased 5.5% to $5.2 billion, lagging the 6.7% growth Wall Street expected. But GAAP net income still jumped 35% to $1.75 per diluted share, easily beating the 15% growth analysts anticipated.</p>
                    <p class="text-start">The bull case for Waste Management centers on its position as the largest waste collection and disposal services provider in North America as measured by revenue. The company also controls 28% of U.S. landfill volume, while its closest competitor controls 20%. Brian Bernard at Morningstar says that advantage is "nearly impossible to replicate given immense regulatory hurdles."</p>
                    <p class="text-start">More broadly, Waste Management provides essential services, meaning demand should remain roughly constant though economic ups and downs. Additionally, its vast network of transfer stations and landfills creates an economic moat that affords the company pricing power. Brian Bernard recently wrote, "Landfill scarcity has supported pricing power for [Waste Management] with annual core price increases consistently exceeding inflation."</p>
