                <h4>Stock exchanges told to improve how they deal with outages</h4>
                <img src="imgs/news2.png" alt="news">
                <div  class="container mx-auto mt-2" style="max-width: 800px">
                    <p class="text-start">LONDON (Reuters) - Stock exchanges and other trading platforms should draw up and make public plans for dealing with outages to provide greater predictability for customers, global securities watchdog IOSCO said on Wednesday.</p>
                    <p class="text-start">Exchanges worldwide have been hit by glitches and outages for various reasons, such as software changes.</p>
                    <p class="text-start">A glitch at the New York Stock Exchange (NYSE) on Monday triggered massive swings in the shares of Berkshire Hathaway and Barrick Gold, and trading halts in dozens of other companies, before the problem was fixed.</p>
                    <p class="text-start">"Where trading venues have effective playbooks and outage plans in place, this provides market participants with certainty about the steps that trading venues will take in the event of an outage," IOSCO, which is comprised of regulators from across the world, said in a report.</p>
                    <p class="text-start">The report highlighted "the need for improved preparedness and management of market outages to ensure market resilience and investor confidence".</p>
                    <p class="text-start">Exchanges could also set out their "reopening strategies" to explain the steps taken for restarting trading and how orders will be managed, the IOSCO 'best practice' guidance said.</p>

